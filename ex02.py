#대입문
a = 1
b = a+1
print(a)
print(b)

#세미콜론으로 대입문을 구분
e = 3.5; f = 5.3
print(e, f)

#여러 변수를 한번에 대입
e, f = 3.5, 5.3
print(e, f)

#여러 개를 같은 값 10으로 대입
x = y = z = 10
print(x, y, z)

#값 교환하기
e, f = f, e
print(e, f)

#변수에 새로운 값을 할당되면 기존의 값을 잃고 새로운 값으로 치환된다.
a = 1
print(type(a))
a = 'hello'
print(type(a))

var01 = 1
print(var01)

var01 = "hello"
print(var01)

